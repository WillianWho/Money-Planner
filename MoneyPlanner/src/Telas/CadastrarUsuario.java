    private void jpConfirmPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jpConfirmPasswordKeyPressed
        if (use.validateField(this.jpPassword, this.jpConfirmPassword) && use.validateField(jpConfirmPassword)) {

            if (use.validateField(this.jtNomeCompleto) && use.validateField(this.jtUsername)) {

                try {
                    Cadastrar cadastrar = new Cadastrar();
                    if(cadastrar.newUserRoot(this.jtNomeCompleto, this.jtUsername, this.jpConfirmPassword)==1){
                        TelaPrincipal tp = new TelaPrincipal(jtNomeCompleto.getText(), "root");
                        this.dispose();
                        tp.setVisible(true);
                    }
                } catch (ClassNotFoundException | SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possível realizar o cadastro:\n " + ex, "Erro", INFORMATION_MESSAGE, null);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Preencha corretamente os campos", "Erro", INFORMATION_MESSAGE, null);
            }
        }
    }//GEN-LAST:event_jpConfirmPasswordKeyPressed

