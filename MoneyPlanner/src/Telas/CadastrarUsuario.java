    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cadastrar cadastrar = new Cadastrar();
        if (use.validateField(this.jtNomeCompleto) && use.validateField(this.jtUsername) && use.validateField(jpPassword) && use.validateField(this.jpPassword, this.jpConfirmPassword)) {

            try {
                cadastrar.newUserRoot(this.jtNomeCompleto, this.jtUsername, this.jpConfirmPassword);
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Não foi possível realizar o cadastro:\n " + ex, "Erro", INFORMATION_MESSAGE, null);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Preencha corretamente os campos", "Erro", INFORMATION_MESSAGE, null);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

